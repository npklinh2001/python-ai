"""
    Yêu cầu người dùng nhập vào 1 chuỗi ký tự bất kỳ
    Kiểm tra xem chuỗi đó có phải là 1 palindrome hay không
    Định nghĩa palindrome: 1 palindrome là 1 từ hoặc số mà
    nếu từ hoặc số đó giữ nguyên giá trị bất kể đọc từ
    trái sang phải hay đọc từ phải sang trái
"""
n = input("Enter a string/number: ")

def is_palindrome(n):
    n_reverse = str(n)[::-1]
    if n == n_reverse:
        return True
    else: 
        return False

print(is_palindrome(n))
